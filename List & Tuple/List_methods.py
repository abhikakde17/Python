#List Methods

list = [4,3,7,9,1,5]

list.append(10) #Adds one element at the end.
print(list)

list.sort() #Sorts in ascending order.
print(list)

list.sort(reverse= True) #Sorts in descending order.
print(list)

list.reverse() #revese the list 
print(list)

list.insert(2, 10) #Inserts element at the index.
print(list)

list.remove(1) #Removes first occurrence of element
print(list)

list.pop(2) #Removes the element at index.
print(list)
