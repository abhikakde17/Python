'''
Tuple:
- A built-in data type that lets us create immutable sequences of values
- Tuple is immutable data type.

'''
tup = (120,110,112,112,150,112)
print(tup[1]) #Returns the element at the index.

print(tup.index(150)) #Returns the index of the occurrence.

print(tup.count(112)) #Counts the total occurrences of element.