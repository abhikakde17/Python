'''Task: 
Given the string `word = "Programming"`, perform the following:
   1. Print the first character of the string.
   2. Print the last character of the string.
   3. Print the length of the string.
'''

word = "Programming"

first_character = word[0]
last_character = word[-1]
length = len(word)

print(f"First Character : {first_character}")
print(f"Last Character: {last_character}")
print(f"Length of String:{length}")